const MOVIES_PER_PAGE = 30;
// Get currentPage from localStorage or default to 1
let currentPage = parseInt(localStorage.getItem('currentPage')) || 1;

const movieList = document.getElementById('movie-list');
const searchInput = document.getElementById('search-input');
const searchButton = document.getElementById('search-button');

function createMovieElement(movie) {
    const movieItem = document.createElement('a');
    movieItem.href = movie.link;
    movieItem.className = 'movie-item';
    movieItem.innerHTML = `
        <img src="${movie.image}" alt="${movie.title}">
        <span>${movie.title}</span>
    `;
    return movieItem;
}

function displayMovies(moviesToShow) {
    const startIndex = (currentPage - 1) * MOVIES_PER_PAGE;
    const endIndex = startIndex + MOVIES_PER_PAGE;
    const paginatedMovies = moviesToShow.slice(startIndex, endIndex);
    
    movieList.innerHTML = '';
    paginatedMovies.forEach(movie => {
        movieList.appendChild(createMovieElement(movie));
    });

    // Update pagination
    updatePagination(moviesToShow.length);
}

function updatePagination(totalMovies) {
    const totalPages = Math.ceil(totalMovies / MOVIES_PER_PAGE);
    const paginationContainer = document.getElementById('pagination');
    paginationContainer.innerHTML = '';

    // Previous button
    if (currentPage > 1) {
        const prevButton = createPaginationButton('Previous', currentPage - 1);
        prevButton.classList.add('pagination-prev');
        paginationContainer.appendChild(prevButton);
    }

    // Page numbers
    for (let i = 1; i <= totalPages; i++) {
        const pageButton = createPaginationButton(i, i);
        if (i === currentPage) {
            pageButton.classList.add('active');
        }
        paginationContainer.appendChild(pageButton);
    }

    // Next button
    if (currentPage < totalPages) {
        const nextButton = createPaginationButton('Next', currentPage + 1);
        nextButton.classList.add('pagination-next');
        paginationContainer.appendChild(nextButton);
    }
}

function createPaginationButton(text, pageNumber) {
    const button = document.createElement('button');
    button.textContent = text;
    button.classList.add('pagination-button');
    button.addEventListener('click', () => {
        currentPage = pageNumber;
        // Store current page in localStorage
        localStorage.setItem('currentPage', currentPage);
        filterMovies();
        // Scroll to top of the page smoothly
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    });
    return button;
}

function filterMovies() {
    const searchTerm = searchInput.value.toLowerCase();
    const filteredMovies = movies.filter(movie =>
        movie.title.toLowerCase().includes(searchTerm)
    );
    displayMovies(filteredMovies);
}

// Event listeners
searchInput.addEventListener('input', () => {
    currentPage = 1; // Reset to first page on search
    localStorage.setItem('currentPage', currentPage);
    filterMovies();
});

searchButton.addEventListener('click', () => {
    currentPage = 1; // Reset to first page on search button click
    localStorage.setItem('currentPage', currentPage);
    filterMovies();
});

// Initial display of movies
filterMovies();

// Inspect Blocker

document.onkeydown = function (e) {
    if (event.keyCode == 123) {
        return false;
    }
    if (e.ctrlKey && e.shiftKey && e.keyCode == 'I'.charCodeAt(0)) {
        return false;
    }
    if (e.ctrlKey && e.shiftKey && e.keyCode == 'J'.charCodeAt(0)) {
        return false;
    }
    if (e.ctrlKey && e.keyCode == 'U'.charCodeAt(0)) {
        return false;
    }
}
document.addEventListener('contextmenu', function (e) {
    e.preventDefault();
});
